function codepoints(x) {
    var res = [];
    for (var i = 0; i < x.length; i++) {
        res.push(x.charCodeAt(i).toString(16));
    }
    return '[' + res.join(' ') + ']';
}

function testOne(x) {
    var idxStart = -1;
    var idxEnd = x.length + 1;

    for (var i = idxStart; i <= idxEnd; i++) {
        for (var j = idxStart; j <= idxEnd; j++) {
            var sub = x.substr(i, j);
            print(codepoints(x), i, j, '->', codepoints(sub));
        }
    }
}

function test() {
    testOne('');
    testOne('foo');
    testOne('foo\u7ffbar');
    testOne('foo\u1234bar');
    testOne('foo\u{1f4a9}bar');
    testOne('\u{1f4a9}foo\u{1f4a9}');
    testOne('\u{1f4a9}');
    testOne('\u07ff');
    testOne('\ufdca');
}

